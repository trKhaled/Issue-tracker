import com.issuetracker.issuetracker.model.Comment
import org.springframework.data.repository.reactive.ReactiveCrudRepository
import org.springframework.stereotype.Repository
import reactor.core.publisher.Flux
import java.util.*

@Repository
interface CommentRepository: ReactiveCrudRepository<Comment, UUID> {
    fun findAllBy(): Flux<Comment>

    fun findByIssueId(issueId: UUID): Flux<Comment>
    fun findByUserId(userId: UUID): Flux<Comment>
}
